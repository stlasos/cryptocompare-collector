CREATE TABLE currency_exchanges (
    from_currency VARCHAR(255) NOT NULL,
    to_currency VARCHAR(255) NOT NULL,
    change24hour DOUBLE,
    change24hour_display TEXT,
	change_ptc24hour DOUBLE,
    change_ptc24hour_display TEXT,
	open24hour DOUBLE,
    open24hour_display TEXT,
	volume24hour DOUBLE,
    volume24hour_display TEXT,
	volume24hour_to DOUBLE,
    volume24hour_to_display TEXT,
	low24hour DOUBLE,
    low24hour_display TEXT,
	high24hour DOUBLE,
    high24hour_display TEXT,
	price DOUBLE,
    price_display TEXT,
	supply DOUBLE,
    supply_display TEXT,
	mktcap DOUBLE,
    mktcap_display TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (from_currency,to_currency)
)